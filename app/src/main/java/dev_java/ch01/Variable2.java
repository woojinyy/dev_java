package dev_java.ch01;

public class Variable2 {
    public static void main(String[] args) {
        System.out.println("소나타"); 
        System.out.println("소나타"); 
        System.out.println("소나타"); 
        String  carName = "소나타";
        System.out.println(carName); 
        System.out.println(carName); 
        System.out.println(carName); 
//변수를 사용하면 변수만 바꾸면 출력값이 다 바뀐다.
//= 일괄처리 가능하다
    }
}    

